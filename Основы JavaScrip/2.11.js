// Напишите функцию isTriangle(a, b, c), 
// которая принимает три числа в качестве аргументов и
//  возвращает true, если они могут быть сторонами треугольника, 
//  и false в противном случае. Условие для треугольника:  
//  сумма любых двух сторон должна быть больше третьей.

function isTriangle(a, b, c){
  return (a + b) > c && (a + c) > b && (b + c) > a
}

// Напишите функцию isLeapYear(year), которая принимает год в 
// качестве аргумента и возвращает true, если он високосный, 
// и false в противном случае. Год является високосным, если он кратен 4, 
// но не кратен 100, за исключением годов, которые делятся на 400.

function isLeapYear(year) {
  return (year % 4 === 0 ) && (year % 100 != 0) || (year % 400 === 0)
}

// Напишите функцию isEvenOrOdd(num), которая 
// принимает число в качестве аргумента и возвращает строку "четное", 
// если число четное, и "нечетное", если число нечетное.

function isEvenOrOdd(num) {
  return num % 2 === 0 ? 'число четное' : 'число нечетное'
}

// Напишите функцию isInRange(num, min, max), 
// которая принимает число и два других числа в качестве аргументов и возвращает true, 
// если число находится включительно между min и max, и false в противном случае.

function isInRange(num, min, max) {
  return num > min && num < max ? true : false
}

